package ex_01_java_basic;

public class Println_vs_Print {
    public static void main(String[] args) {
        System.out.println("hello first line");  // print with new line
        System.out.println("hello second line"); // print with new line

        System.out.print("Hello First");
        System.out.println("Hello Second"); // print in same line
    }
}

package ex_10_For_Loop;

public class For_If_Condition {
    public static void main(String[] args) {
        for(int s=0; s < 18; s++){ // 0 to 17, 18 times
            if (s > 15){
                System.out.println("Gift from Papa, IPhone");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}

package ex_07_Increment_Decrement_Operator;

public class In_De_Op_02 {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a; // first increase and assign the value to result
        // 10+1 = 11
        System.out.println(b);
        System.out.println(a);
        //  Exp and Result Table
        // Line No | a | Result b
        // 8 | 10 | NA
        // 9 | 11 | 11
        // 10 | 11(NA) | 11
        // 11 | 11 | 11(NA)


    }
}
